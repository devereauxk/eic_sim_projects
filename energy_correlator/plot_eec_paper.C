      temp->GetXaxis()->SetTitle("R_{L}#sqrt{p_{T,jet}}");
      temp->GetYaxis()->SetTitle("normalized EEC (rel. norm. * on - off)");
      temp->SetMarkerColor(pt_color[ipt]);
      temp->SetLineColor(pt_color[ipt]);
